/*
    = assign operator
    == comparison operator
    === strict equality operator (data type must match)
    != inequality operator
    !== strict inequality operator (data type must match)
*/

const PI = 3.14;

if(PI === "3.14") {
    console.log("That is PI");
}

else {
    console.log("That is NOT PI");
}

if(PI !== "3.14") {
    console.log("That is NOT PI");
}

else {
    console.log("That is PI");
}